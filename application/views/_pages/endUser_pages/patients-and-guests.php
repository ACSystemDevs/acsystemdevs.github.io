					<div class="tab-pane container mt-4 active" id="prr-1">
						<h3>1. Right to Medical Care of Good Quality</h3>
						<p>
							<ol type="a">
								<li>Every person is entitled without discrimination to appropriate medical care. </li>
								<li>Every patient has the right to be cared for by a physician whom he/she knows to be free to make clinical and ethical judgements without any outside interference.</li>
								<li>The patient shall always be treated in accordance with his/her best interests. The treatment applied shall be in accordance with generally approved medical principles.</li>
								<li>Quality assurance should always be a part of health care. Physicians, in particular, accept responsibility for being guardians of the quality of medical services.</li>
								<li>In circumstances where a choice must be made between potential patients for a particular treatment that is in limited supply, all such patients are entitled to a fair selection procedure for that treatment. That choice must be based on medical criteria and made without discrimination.</li>
								<li>The patient has the right to continuity of health care. The physician has an obligation to cooperate in the coordination of medically indicated care with other health care providers treating the patient. The physician may not discontinue treatment of a patient as long as further treatment is medically indicated, without giving the patient reasonable assistance and sufficient opportunity to make alternative arrangements for care.</li>
							</ol>
						</p>
					</div>

					<div class="tab-pane container mt-4 mt-4 fade" id="prr-2">
						<h3>2. Right to Freedom of Choice</h3>
						<p>
							<ol type="a">
								<li>The patient has the right to choose freely and change his/her physician and hospital or health service institution, regardless of whether they are based in the private or public sector.</li>
								<li>The patient has the right to ask for the opinion of another physician at any stage.</li>
							</ol>
						</p>
					</div>

					<div class="tab-pane container mt-4 fade" id="prr-3">
						<h3>3. Right to Self-Determination</h3>
						<p>
							<ol type="a">
								<li>The patient has the right to self-determination, to make free decisions regarding himself/herself. The physician will inform the patient of the consequences of his/her decisions.</li>
								<li>A mentally competent adult patient has the right to give or withhold consent to any diagnostic procedure or therapy. The patient has the right to the information necessary to make his/her decisions. The patient should understand clearly what is the purpose of any test or treatment, what the results would imply, and what would be the implications of withholding consent.</li>
								<li>The patient has the right to refuse to participate in research or the teaching of medicine.</li>
							</ol>
						</p>
					</div>

					<div class="tab-pane container mt-4 fade" id="prr-4">
						<h3>4. The Unconscious Patient</h3>
						<p>
							<ol type="a">
								<li>If the patient is unconscious or otherwise unable to express his/her will, informed consent must be obtained whenever possible, from a legally entitled representative.</li>
								<li>If a legally entitled representative is not available, but a medical intervention is urgently needed, consent of the patient may be presumed, unless it is obvious and beyond any doubt on the basis of the patient's previous firm expression or conviction that he/she would refuse consent to the intervention in that situation.</li>
								<li>However, physicians should always try to save the life of a patient unconscious due to a suicide attempt. </li>
							</ol>
						</p>
					</div>

					<div class="tab-pane container mt-4 fade" id="prr-5">
						<h3>5. The Legally Incompetent Patient</h3>
						<p>
							<ol type="a">
								<li>If a patient is a minor or otherwise legally incompetent, the consent of a legally entitled representative is required in some jurisdictions. Nevertheless the patient must be involved in the decision-making to the fullest extent allowed by his/her capacity.</li>
								<li>If the legally incompetent patient can make rational decisions, his/her decisions must be respected, and he/she has the right to forbid the disclosure of information to his/her legally entitled representative. </li>
								<li>If the patient's legally entitled representative, or a person authorized by the patient, forbids treatment which is, in the opinion of the physician, in the patient's best interest, the physician should challenge this decision in the relevant legal or other institution. In case of emergency, the physician will act in the patient's best interest.</li>
							</ol>
						</p>
					</div>

					<div class="tab-pane container mt-4 fade" id="prr-6">
						<h3>6. Procedures Against the Patient's Will</h3>
						<p>
							<ol type="a">
								<li>Diagnostic procedures or treatment against the patient's will can be carried out only in exceptional cases, if specifically permitted by law and conforming to the principles of medical ethics.</li>
							</ol>
						</p>
					</div>

					<div class="tab-pane container mt-4 fade" id="prr-7">
						<h3>7. Right to Information</h3>
						<p>
							<ol type="a">
								<li>The patient has the right to receive information about himself/herself recorded in any of his/her medical records, and to be fully informed about his/her health status including the medical facts about his/her condition. However, confidential information in the patient's records about a third party should not be given to the patient without the consent of that third party.</li>
								<li>Exceptionally, information may be withheld from the patient when there is good reason to believe that this information would create a serious hazard to his/her life or health. </li>
								<li>Information should be given in a way appropriate to the patient’s culture and in such a way that the patient can understand.</li>
								<li>The patient has the right not to be informed on his/her explicit request, unless required for the protection of another person's life.</li>
								<li>The patient has the right to choose who, if anyone, should be informed on his/her behalf.</li>
							</ol>
						</p>
					</div>

					<div class="tab-pane container mt-4 fade" id="prr-8">
						<h3>8. Right to Confidentiality.</h3>
						<p>
							<ol type="a">
								<li>All identifiable information about a patient's health status, medical condition, diagnosis, prognosis and treatment and all other information of a personal kind must be kept confidential, even after death. Exceptionally, descendants may have a right of access to information that would inform them of their health risks.</li>
								<li>Confidential information can only be disclosed if the patient gives explicit consent or if expressly provided for in the law. Information can be disclosed to other health care providers only on a strictly "need to know" basis unless the patient has given explicit consent.</li>
								<li>All identifiable patient data must be protected. The protection of the data must be appropriate to the manner of its storage. Human substances from which identifiable data can be derived must be likewise protected.</li>
							</ol>
						</p>
					</div>

					<div class="tab-pane container mt-4 fade" id="prr-9">
						<h3>9. Right to Health Education</h3>
						<p>
							<ol type="a">
								<li>Every person has the right to health education that will assist him/her in making informed choices about personal health and about the available health services. The education should include information about healthy lifestyles and about methods of prevention and early detection of illnesses. The personal responsibility of everybody for his/her own health should be stressed. Physicians have an obligation to participate actively in educational efforts.</li>
							</ol>
						</p>
					</div>

					<div class="tab-pane container mt-4 fade" id="prr-10">
						<h3>10. Right to Dignity.</h3>
						<p>
							<ol type="a">
								<li>The patient's dignity and right to privacy shall be respected at all times in medical care and teaching, as shall his/her culture and values.</li>
								<li>The patient is entitled to relief of his/her suffering according to the current state of knowledge.</li>
								<li>The patient is entitled to humane terminal care and to be provided with all available assistance in making dying as dignified and comfortable as possible.</li>
							</ol>
						</p>
					</div>

					<div class="tab-pane container mt-4 fade" id="prr-11">
						<h3>11. Right to Religious Assistance</h3>
						<p>
							<ol type="a">
								<li>The patient has the right to receive or to decline spiritual and moral comfort including the help of a minister of his/her chosen religion.</li>
							</ol>
						</p>
					</div>

					<div class="tab-pane container mt-4 fade" id="prr-12">
						<h3>12. Right to Informed Consent</h3>
						<p>
							<ol type="a">
								<li>No reputable practitioner or facility that performs tests, procedures or treatments will do so without asking the patient or his guardian to sign a form giving consent. This document is called "informed consent" because the practitioner is expected to provide clear explanations of the risks and benefits prior to the patient's participation, although that does not always happen as thoroughly as it should.</li>
							</ol>
						</p>
					</div>

					<div class="tab-pane container mt-4 fade" id="prr-13">
						<h3>13. Right to Refuse Treatment</h3>
						<p>
							<ol type="a">
								<li>A patient may refuse treatment as long as he is considered to be capable of making sound decisions, or he made that choice when he was of sound mind through written expression (as is often the case when it comes to end-of-life care). This includes the right to refuse surgery.</li>
							</ol>
						</p>
					</div>
				</div>
