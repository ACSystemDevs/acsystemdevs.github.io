				<div class="col-sm-12 pr-md-5 pl-md-5">
					<div class="row mt-1">
						<p class="text-justify">
							Bethany Hospital carry a proud heritage with humble beginnings. Like many other worthy causes, the hospital started as an idea that is to provide medical care for the people who could otherwise die of disease if not treated with adequate hospital care. This worthy idea came from a non- medical missionary named Dr. Howard W. Widdoes from the United Brethren Church of America in 1904. However, it took about 17 years before a hospital was established.  On August 1, 1921, a 6 bed “sawali” made medical infirmary was organized by Miss Clara Mann who was sent by the board of the United Brethren Church. This medical infirmary was housed in the old Bible Training School behind the Capitol Church. The fund campaign for a hospital building was started by Dr. Widdoes in his furlough to the United States in 1909-1910. However, it took 5 years later when an anonymous industrialist gave a gift of $30,000 for the realization of the project. This gift was augmented by P14,000 from both Catholic and Protestant friends here in San Fernando, La Union.
							<br><br>
							On Hospital day in May 1928, after 1 year of construction, a modern hospital building made of reinforced concrete with earthquake specification was opened. At that time, Bethany hospital was considered as a primary referral center for patients coming not only from La Union but also from the neighboring provinces as well.
							<br><br>
							At the present time, Bethany Hospital, Inc., a 99-year old medical facility, is a private Level II Tertiary Referral Center, located at the City of San Fernando, La Union. Through the years, Bethany Hospital was able to progress and develop into one of the more modern hospitals in the Ilocos Region.
